export function t(lang, key, params = {}) {
  const keys = key.split('.')
  let value = translations[lang] || translations.en

  for (const k of keys) {
    value = value?.[k]
  }

  if (typeof value !== 'string') return key

  return Object.entries(params).reduce(
    (str, [k, v]) => str.replace(`{${k}}`, String(v)),
    value
  )
}
